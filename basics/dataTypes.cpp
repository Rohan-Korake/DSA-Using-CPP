#include <iostream>
using namespace std;

int main(){
    int intergerVar=10;
    float floatingVar=10.5;
    char characterVar='R';
    bool booleanVar=true;
    double doubleVar=1212.99;

    cout<<"Integer value is "<<intergerVar<<" and size is 4 byte"<<endl;
    cout<<"Floating value is "<<floatingVar<<" and size is 4 byte"<<endl;
    cout<<"Character value is "<<characterVar<<" and size is 1 byte"<<endl;
    cout<<"Boolean value is "<<booleanVar<<" and size is 1 byte"<<endl;
    cout<<"Double value is "<<doubleVar<<" and size is 8 byte"<<endl;
return 0;
}