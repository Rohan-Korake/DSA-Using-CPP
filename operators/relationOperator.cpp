#include <iostream>
using namespace std;

int main()
{
    cout<<"10 == 20 is " <<(10==20)<<endl; // return false (0)
    cout<<"10 != 20 is " <<(10!=20)<<endl; // return true (1)
    cout<<"10 <  20 is " <<(10<20)<<endl; // return true (1)
    cout<<"10 >  20 is " <<(10>20)<<endl; // return false (0)
    cout<<"10 <= 20 is " <<(10<=20)<<endl; // return true (1)
    cout<<"10 >= 20 is " <<(10>=20)<<endl; // return false (0)
    return 0;
}